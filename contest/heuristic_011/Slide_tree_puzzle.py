def path_list(t):
    path_dict = {'0': [0, 0, 0, 0],     # [left, up, right, down]
                 '1': [1, 0, 0, 0],
                 '2': [0, 1, 0, 0],
                 '3': [1, 1, 0, 0],
                 '4': [0, 0, 1, 0],
                 '5': [1, 0, 1, 0],
                 '6': [0, 1, 1, 0],
                 '7': [1, 1, 1, 0],
                 '8': [0, 0, 0, 1],
                 '9': [1, 0, 0, 1],
                 'a': [0, 1, 0, 1],
                 'b': [1, 1, 0, 1],
                 'c': [0, 0, 1, 1],
                 'd': [1, 0, 1, 1],
                 'e': [0, 1, 1, 1],
                 'f': [1, 1, 1, 1]}

    return path_dict[t]


class Tile:
    def __init__(self, i, j, t):
        self.i = i
        self.j = j
        self.path = path_list(t)

